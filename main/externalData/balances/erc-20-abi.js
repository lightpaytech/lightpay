/**
 * Validates a single ABI entry object.
 * Returns true if the entry has the minimum required fields for a function or event.
 *
 * @param {object} entry - An ABI entry object
 * @returns {boolean}
 */
export function validateAbiEntry(entry) {
  if (!entry || typeof entry !== 'object') return false

  const validTypes = ['function', 'event', 'constructor', 'fallback', 'receive']

  if (!validTypes.includes(entry.type)) return false

  if (entry.type === 'function' || entry.type === 'event') {
    if (typeof entry.name !== 'string' || entry.name.length === 0) return false
  }

  if (entry.inputs !== undefined && !Array.isArray(entry.inputs)) return false
  if (entry.outputs !== undefined && !Array.isArray(entry.outputs)) return false

  return true
}

